    public static String encrypt(String data, int length) throws Exception{
    	String result = encrypt(data, KEY).toUpperCase().replaceAll("\\W", "U");
    	int len = result.length();
    	if(len < length){
    		for(int i = 0;i<length - len;i++){
    			result += "R";
    		}
    	}else if(result.length() > length){
    		result = result.substring(0, length);
    	}
    	return result;
    }
    
